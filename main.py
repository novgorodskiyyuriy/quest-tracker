# Импортируем класс FastAPI из библиотеки fastapi
from fastapi import FastAPI

# Создаем экземпляр нашего приложения. 
# app - это и есть наш "мозг", к которому мы будем добавлять маршруты.
app = FastAPI()

# Это "декоратор". Он говорит FastAPI: 
# "Когда кто-то зайдет на главную страницу (корень сайта '/'), 
# используй функцию, которая написана прямо под тобой".
@app.get("/")
async def root():
    # Функция просто возвращает словарь. 
    # FastAPI сам превратит этот словарь Python в формат JSON, 
    # который поймет любой браузер или iPhone.
    return {"message": "Привет! Сервер Quest Tracker работает!"}